   public boolean updateStudent(){
       boolean isUpdated=false;
           
         try{
             Connection con =DBConnection.getConnection();
             
                                                                                                                                                                 
             PreparedStatement pst=con.prepareStatement("update student_details set name='"+text_studentName.getText()+"',course='"+combo_CourseName.getSelectedItem()+"',branch='"+combo_branch.getSelectedItem()+"'where student_id="+text_studentId.getText());
            
            
            
             int rowCount=pst.executeUpdate();
             if(rowCount > 0){
                 text_studentName.setText("");
                 combo_CourseName.setSelectedItem("");
                 combo_branch.setSelectedItem("");
                 text_studentId.setText("");
                 
                 
                 isUpdated=true;
             }
             else
             {
                 isUpdated=false;
              }
                     
         }catch(Exception e){
           e.printStackTrace();
         }
         return isUpdated;
   }
   public boolean deleteStudent(){
       boolean isDeleted= false;
      int  studentId= Integer.parseInt(text_studentId.getText());
      try{
          Connection con =DBConnection.getConnection();
          String sql="delete from student_details where student_id=?";
          PreparedStatement pst=con.prepareStatement(sql);
         pst.setInt(1, studentId);
         int rowCount= pst.executeUpdate();
         if(rowCount>0){
             isDeleted=true;
             
         }
         else{
             isDeleted=false;
         }
          
      }catch(Exception e){
          e.printStackTrace();
      }
      return isDeleted;
   }
   public void clearTable(){
       DefaultTableModel model=(DefaultTableModel )tbl_studentDetails.getModel();
       model.setRowCount(0);
   }
