   public void setStudentDetailsToTable(){
       try{
          Class.forName("com.mysql.cj.jdbc.Driver");
	Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","root");
	
	Statement ps=con.createStatement();
	ResultSet rs=ps.executeQuery("select*from student_details");
	
         while(rs.next()) {
             String StudentId =rs.getString("student_id");
             String StudentName =rs.getString("name");
             String course =rs.getString("course");
             String branch=rs.getString("branch");
             Object[] obj ={StudentId,StudentName,course,branch};
             model=(DefaultTableModel) tbl_studentDetails.getModel();
             model.addRow(obj);
         }  
       }
       catch(Exception e){
           e.printStackTrace();
       }
   }
   public boolean addStudent(){
       boolean isAdded=false;
            int StudentId=Integer.parseInt(text_studentId.getText());
            String studentName=text_studentName.getText();
            String course =combo_CourseName.getSelectedItem().toString();
           String branch=combo_branch.getSelectedItem().toString();
             try{
             Connection con=DBConnection.getConnection();
             String sql="insert into student_details values(?,?,?,?)";
             PreparedStatement pst=con.prepareStatement(sql);
             pst.setInt(1, StudentId);
             pst.setString(2, studentName);
             pst.setString(3, course);
             pst.setString(4, branch);
             int rowCount=pst.executeUpdate();
             if(rowCount>0){
                 isAdded=true;
             }
             else{
                 isAdded=false;
             }
             
             }
             catch(Exception e){
                e.printStackTrace();
             }
             return isAdded;
   }
